#!/usr/bin/python3

# This is a basic list containing integers
list1 = [1, 3, 5, 11]

# Lists can also contain strings, or can mix types
list2 = [1, 'abc', 'efg', 3.5]

# Here's a list of even numbers built with range
list3 = list(range(2, 22, 2))

# You can past lists to the print function just like
# simple variables. They'll print their items inside of square
# brackets seperated by commas.
print('list1=', list1)
print('list2=', list2)
print('list3=', list3)
